def parse_args(argv: list[str]) -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--repo-root", type=Path, default=REPO_ROOT)
    parser.add_argument("--out-dir", type=Path, default=DEFAULT_OUT_DIR)
    return parser.parse_args(argv)


def validate(rows: tuple[SatelliteRow, ...], repo_root: Path) -> list[str]:
    errors = []
    for row in rows:
        for support_file in row.support_files:
            if not (repo_root / support_file).exists():
                errors.append(f"{row.lane}: missing support file {support_file}")
        if "no " not in row.non_promotion_boundary.lower():
            errors.append(f"{row.lane}: non-promotion boundary must be explicit")
    return errors


def render_markdown(rows: tuple[SatelliteRow, ...]) -> str:
    lines = [
        "# Satellite paper routing index",
        "",
        "Generated by `scripts/generate_satellite_paper_index.py`.",
        "Satellite lanes are staged after Papers 1, 3, and 8 and must not change Clay-facing promotion flags.",
        "",
        "| lane | publishability tier | support files | non-promotion boundary | dependency on core papers |",
        "| --- | --- | --- | --- | --- |",
    ]
    for row in rows:
        files = "<br>".join(f"`{path}`" for path in row.support_files)
        lines.append(
            "| "
            + " | ".join(
                [
                    row.lane,
                    row.publishability_tier,
                    files,
                    row.non_promotion_boundary,
                    row.dependency_on_core_papers,
                ]
            )
            + " |"
        )
    return "\n".join(lines) + "\n"


def display_path(path: Path, repo_root: Path) -> str:
    try:
        return path.relative_to(repo_root).as_posix()
    except ValueError:
        return path.as_posix()


def main(argv: list[str] | None = None) -> int:
    args = parse_args(sys.argv[1:] if argv is None else argv)
    repo_root = args.repo_root.resolve()
    out_dir = args.out_dir if args.out_dir.is_absolute() else repo_root / args.out_dir

    errors = validate(ROWS, repo_root)
    if errors:
        for error in errors:
            print(f"generate_satellite_paper_index: {error}", file=sys.stderr)
        return 1

    out_dir.mkdir(parents=True, exist_ok=True)
    payload = {
        "generator": "scripts/generate_satellite_paper_index.py",
        "row_count": len(ROWS),
        "rows": [asdict(row) for row in ROWS],
    }
    (out_dir / f"{BASE_NAME}.json").write_text(
        json.dumps(payload, indent=2, sort_keys=True) + "\n",
        encoding="utf-8",
    )
    (out_dir / f"{BASE_NAME}.md").write_text(render_markdown(ROWS), encoding="utf-8")
    print(f"wrote {len(ROWS)} satellite rows")
    print(display_path(out_dir / f"{BASE_NAME}.json", repo_root))
    print(display_path(out_dir / f"{BASE_NAME}.md", repo_root))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())

#!/usr/bin/env python3
from __future__ import annotations

import argparse
import hashlib
import re
from pathlib import Path


DEFAULT_NUM_TEMPLATE = Path("temp-DOWNLOADED/dyturbo/run_cms_smoke/t43_num_wi_20k5_serial.in")
DEFAULT_DEN_TEMPLATE = Path("temp-DOWNLOADED/dyturbo/run_cms_smoke/t43_den_wi_20k5_serial.in")
DEFAULT_FPC_TEMPLATE = Path("temp-DOWNLOADED/dyturbo/source/dyturbo-1.4.2/input/default.in")
DEFAULT_OUTPUT_DIR = Path("temp-DOWNLOADED/dyturbo/run_cms_smoke/fpc_smoke")
DEFAULT_USER_CUTS = Path("temp-DOWNLOADED/dyturbo/source/dyturbo-1.4.2/user/user_cuts.h")
EXACT_CMS_MARKER = "DASHI_CMS_SMP_20_003_USER_CUTS_LEAD25_SUBLEAD20_ABSETA24"

FPC_KEYS = {
    "doFPC",
    "FPCquad",
    "intDimFPC",
    "qtfpc",
    "qtrec_naive",
    "qtrec_cs",
    "qtrec_kt0",
}

CARD_KEYS = {
    "sroot",
    "ih1",
    "ih2",
    "nproc",
    "fixedorder_only",
    "doBORN",
    "doCT",
    "doVJ",
    "BORNquad",
    "CTquad",
    "VJquad",
    "cores",
    "makecuts",
    "lptcut",
    "lycut",
    "lepptcut",
    "lepycut",
    "alpptcut",
    "alpycut",
    "lcptcut",
    "lfptcut",
    "output_filename",
    "makehistos",
    "ptbinwidth",
    "ybinwidth",
    "mbinwidth",
    "qt_bins",
    "y_bins",
    "m_bins",
}

ALL_REQUIRED_KEYS = CARD_KEYS | FPC_KEYS


def _parse_assignments(text: str) -> dict[str, str]:
    values = {}
    for line in text.splitlines():
        match = re.match(r"^\s*([A-Za-z_][A-Za-z0-9_]*)\s*=\s*([^#\n]*)(.*)$", line)
        if match:
            values[match.group(1)] = match.group(2).strip()
    return values


def _key_pattern(key: str) -> re.Pattern[str]:
    return re.compile(rf"^(\s*{re.escape(key)}\s*=\s*)([^#\n]*)(.*)$", re.MULTILINE)


def _replace_value(text: str, key: str, value: str) -> str:
    pattern = _key_pattern(key)
    if not pattern.search(text):
        raise KeyError(key)

    def repl(match: re.Match[str]) -> str:
        suffix = match.group(3)
        if suffix.startswith("#"):
            suffix = "  " + suffix
        return f"{match.group(1)}{value}{suffix}"

    return pattern.sub(repl, text, count=1)


def _require_keys(label: str, values: dict[str, str], keys: set[str]) -> None:
    missing = sorted(keys - set(values))
    if missing:
        raise SystemExit(f"{label} is missing required DYTurbo keys: {', '.join(missing)}")


def _sha256_prefix(path: Path) -> str:
    digest = hashlib.sha256()
    with path.open("rb") as handle:
        for chunk in iter(lambda: handle.read(1024 * 1024), b""):
            digest.update(chunk)
    return digest.hexdigest()[:16]


def _verify_exact_user_cuts(cut_mode: str, user_cuts_header: str) -> str | None:
    if cut_mode == "stock-symmetric":
        return None
    path = Path(user_cuts_header)
    if not path.exists():
        raise SystemExit(f"blocked: exact CMS cut mode requires patched user_cuts.h: {path}")
    text = path.read_text(encoding="utf-8", errors="ignore")
    if EXACT_CMS_MARKER not in text:
        raise SystemExit(
            "blocked: exact CMS cut mode requires the patched user_cuts.h marker "
            f"{EXACT_CMS_MARKER}"
        )
    return _sha256_prefix(path)


def _render_card(
    card_template: str,
    fpc_values: dict[str, str],
    output_name: str,
    calls: int,
    exact_cms: bool,
) -> str:
    text = card_template
    replacements = {
        "fixedorder_only": "true",
        "doBORN": "true",
        "doCT": "true",
        "doVJ": "true",
        "doFPC": "true",
        "BORNquad": "false",
        "CTquad": "false",
        "VJquad": "false",
        "FPCquad": "false",
        "intDimFPC": "8",
        "qtfpc": fpc_values["qtfpc"],
        "qtrec_naive": "false",
        "qtrec_cs": "true",
        "qtrec_kt0": "false",
        "cores": "0",
        "makecuts": "true",
        "lptcut": "20" if exact_cms else "25",
        "output_filename": output_name,
        "makehistos": "true",
        "vegasncallsBORN": str(calls),
        "vegasncallsCT": str(calls),
        "vegasncallsVJLO": str(calls),
        "vegasncallsVJREAL": str(calls),
        "vegasncallsVJVIRT": str(calls),
    }
    for key, value in replacements.items():
        text = _replace_value(text, key, value)
    return (
        "# Generated by scripts/prepare_dyturbo_t43_fpc_cards.py\n"
        + (
            f"# Exact CMS candidate card. Requires patched user_cuts.h marker: {EXACT_CMS_MARKER}\n"
            if exact_cms
            else "# Non-promoting FPC smoke card. Uses stock DYTurbo symmetric lepton cuts; not exact CMS leading/subleading cuts.\n"
        )
        + "# Serial enforced: cores = 0.\n\n"
        + text
    )


def prepare(args: argparse.Namespace) -> list[Path]:
    user_cuts_hash = _verify_exact_user_cuts(args.cut_mode, args.user_cuts_header)
    exact_cms = args.cut_mode == "exact-cms"

    num_path = Path(args.num_template)
    den_path = Path(args.den_template)
    fpc_path = Path(args.fpc_template)
    for path in (num_path, den_path, fpc_path):
        if not path.exists():
            raise SystemExit(f"template not found: {path}")

    num_text = num_path.read_text(encoding="utf-8")
    den_text = den_path.read_text(encoding="utf-8")
    fpc_text = fpc_path.read_text(encoding="utf-8")
    num_values = _parse_assignments(num_text)
    den_values = _parse_assignments(den_text)
    fpc_values = _parse_assignments(fpc_text)

    _require_keys("numerator template", num_values, CARD_KEYS)
    _require_keys("denominator template", den_values, CARD_KEYS)
    _require_keys("FPC template", fpc_values, FPC_KEYS)

    # The t43 templates carry the CMS-SMP-20-003 phi* bins and mass windows.
    if num_values["m_bins"] == den_values["m_bins"]:
        raise SystemExit("blocked: numerator and denominator templates have identical m_bins")
    if num_values["qt_bins"] != den_values["qt_bins"]:
        raise SystemExit("blocked: numerator and denominator templates have different qt_bins")
    if num_values["cores"] != "0" or den_values["cores"] != "0":
        raise SystemExit("blocked: source t43 templates are not serial cores=0 cards")

    output_dir = Path(args.output_dir)
    outputs = [
        (
            output_dir / ("t43_num_fpc_exactcms_serial.in" if exact_cms else "t43_num_fpc_smoke_serial.in"),
            num_text,
            "dyturbo_t43_num_fpc_exactcms_serial" if exact_cms else "dyturbo_t43_num_fpc_smoke_serial",
        ),
        (
            output_dir / ("t43_den_fpc_exactcms_serial.in" if exact_cms else "t43_den_fpc_smoke_serial.in"),
            den_text,
            "dyturbo_t43_den_fpc_exactcms_serial" if exact_cms else "dyturbo_t43_den_fpc_smoke_serial",
        ),
    ]
    if args.dry_run:
        return [path for path, _text, _name in outputs]

    output_dir.mkdir(parents=True, exist_ok=True)
    written = []
    for path, template, name in outputs:
        path.write_text(_render_card(template, fpc_values, name, args.calls, exact_cms), encoding="utf-8")
        written.append(path)
    if user_cuts_hash:
        proof_path = output_dir / "exact_cms_user_cuts.sha256_16"
        proof_path.write_text(user_cuts_hash + "\n", encoding="utf-8")
        written.append(proof_path)
    return written


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Prepare fail-closed DYTurbo t43 FPC smoke cards from existing templates."
    )
    parser.add_argument("--num-template", default=str(DEFAULT_NUM_TEMPLATE))
    parser.add_argument("--den-template", default=str(DEFAULT_DEN_TEMPLATE))
    parser.add_argument("--fpc-template", default=str(DEFAULT_FPC_TEMPLATE))
    parser.add_argument("--output-dir", default=str(DEFAULT_OUTPUT_DIR))
    parser.add_argument("--calls", type=int, default=20000)
    parser.add_argument("--user-cuts-header", default=str(DEFAULT_USER_CUTS))
    parser.add_argument(
        "--cut-mode",
        choices=("exact-cms", "stock-symmetric"),
        default="exact-cms",
        help="exact-cms fails closed; stock-symmetric emits non-promoting smoke cards using existing t43 cuts.",
    )
    parser.add_argument("--dry-run", action="store_true")
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    paths = prepare(args)
    for path in paths:
        print(path)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())

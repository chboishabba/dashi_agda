#!/usr/bin/env python3
"""Numerical harness for the defect four-point/parallelogram frontier.

This is a diagnostic sampler only.  It does not prove or certify any theorem.
It compares candidate defect functionals against the exact residual recorded in
the Agda boundary:

    Q(x + y) + Q(x - y) - 2 Q(x) - 2 Q(y).

The useful signal is negative: l1-like and p-norm-like defects can satisfy
basic quotient/monotonicity/hierarchy-looking checks while still failing the
four-point law.  Coupling is not the issue by itself: a coupled quadratic still
passes the residual exactly up to floating-point noise.
"""

from __future__ import annotations

import argparse
import json
import math
import random
import statistics
from dataclasses import asdict, dataclass
from typing import Callable, List, Optional, Sequence, Tuple

Vector = List[float]
Defect = Callable[[Vector], float]


@dataclass(frozen=True)
class Functional:
    name: str
    q: Defect
    expected: str


@dataclass
class Metrics:
    name: str
    classification: str
    mean_abs_four_point: float
    max_abs_four_point: float
    p95_abs_four_point: float
    mean_signed_four_point: float
    min_value: float
    max_zero_error: float
    max_even_error: float
    max_two_homogeneity_error: float
    nonnegativity_violations: int
    monotonicity_violations: int
    max_monotonicity_violation: float
    hierarchy_c_needed_max: float
    hierarchy_violations: int
    coarse_gate_failures: Tuple[str, ...]
    passes_coarse_hierarchy_like_gate: bool
    fails_only_four_point_after_coarse_gate: bool
    failed_hypotheses: Tuple[str, ...]
    expected: str


def add(x: Sequence[float], y: Sequence[float]) -> Vector:
    return [a + b for a, b in zip(x, y)]


def sub(x: Sequence[float], y: Sequence[float]) -> Vector:
    return [a - b for a, b in zip(x, y)]


def scale(t: float, x: Sequence[float]) -> Vector:
    return [t * a for a in x]


def dot(x: Sequence[float], y: Sequence[float]) -> float:
    return sum(a * b for a, b in zip(x, y))


def l2_sq(x: Sequence[float]) -> float:
    return dot(x, x)


def l1_squared(x: Sequence[float]) -> float:
    return sum(abs(a) for a in x) ** 2


def max_norm_squared(x: Sequence[float]) -> float:
    return max((abs(a) for a in x), default=0.0) ** 2


def p_norm_squared(p: float) -> Defect:
    def q(x: Vector) -> float:
        return sum(abs(a) ** p for a in x) ** (2.0 / p)

    return q


def coupled_quadratic_spd(x: Sequence[float]) -> float:
    # SPD tridiagonal form: diagonal dominance makes the coupling harmless.
    value = 1.4 * sum(a * a for a in x)
    value += 0.35 * sum(x[i] * x[i + 1] for i in range(len(x) - 1))
    return value


def coupled_nonquadratic(x: Sequence[float]) -> float:
    # Positive, even, monotone under shrink in most samples, but not 2-homogeneous.
    base = l2_sq(x)
    pair_coupling = sum((x[i] * x[i + 1]) ** 2 for i in range(len(x) - 1))
    return base + 0.25 * pair_coupling


def coupled_l1_l2(x: Sequence[float]) -> float:
    # Two-homogeneous and nonnegative, but not generated by an inner product.
    return l2_sq(x) + 0.35 * l1_squared(x)


def random_spd_quadratic(dim: int, rng: random.Random) -> Defect:
    """Build a reproducible SPD quadratic form from a random Gram matrix."""
    rows = [[rng.gauss(0.0, 1.0) for _ in range(dim)] for _ in range(dim)]
    ridge = 0.25

    def q(x: Vector) -> float:
        y = [sum(row[j] * x[j] for j in range(dim)) for row in rows]
        return sum(v * v for v in y) + ridge * l2_sq(x)

    return q


def random_polyhedral_norm_squared(dim: int, rng: random.Random, facets: int) -> Defect:
    """Squared centrally symmetric polyhedral norm max_i |a_i.x|^2."""
    directions: List[Vector] = []
    for _ in range(facets):
        v = random_vector(dim, rng)
        norm = math.sqrt(l2_sq(v))
        if norm <= 1.0e-14:
            v[0] = 1.0
            norm = 1.0
        directions.append([a / norm for a in v])

    def q(x: Vector) -> float:
        return max(abs(dot(v, x)) for v in directions) ** 2

    return q


def mixed_two_homogeneous(dim: int, rng: random.Random) -> Defect:
    """Quadratic plus two non-Hilbert two-homogeneous pieces."""
    poly = random_polyhedral_norm_squared(dim, rng, facets=max(2 * dim, 6))

    def q(x: Vector) -> float:
        return l2_sq(x) + 0.20 * l1_squared(x) + 0.30 * poly(x)

    return q


def random_vector(dim: int, rng: random.Random) -> Vector:
    return [rng.gauss(0.0, 1.0) for _ in range(dim)]


def four_point_residual(q: Defect, x: Vector, y: Vector) -> float:
    return q(add(x, y)) + q(sub(x, y)) - 2.0 * q(x) - 2.0 * q(y)


def safe_ratio(numerator: float, denominator: float) -> float:
    if denominator <= 1.0e-14:
        return 0.0 if numerator <= 0.0 else math.inf
    return numerator / denominator


def percentile(values: Sequence[float], pct: float) -> float:
    if not values:
        return 0.0
    ordered = sorted(values)
    idx = min(len(ordered) - 1, max(0, round((len(ordered) - 1) * pct)))
    return ordered[idx]


def classify(
    failures: Sequence[str],
    max_residual: float,
    max_homogeneity_error: float,
    hierarchy_violations: int,
) -> str:
    if not failures:
        return "pass_quadratic_candidate"
    if (
        failures == ["four_point_parallelogram"]
        or set(failures) == {"hierarchy_consistency", "four_point_parallelogram"}
    ) and max_homogeneity_error <= 1.0e-7:
        return "near_miss_two_homogeneous_counterexample"
    if "four_point_parallelogram" in failures and hierarchy_violations == 0:
        return "counterexample_to_coarse_checks"
    if "q_nat_square_homogeneous" in failures:
        return "reject_nonquadratic_scaling"
    if max_residual > 1.0e-7:
        return "counterexample"
    return "reject_basic_hypotheses"


def sample_metrics(
    functional: Functional,
    dim: int,
    samples: int,
    rng: random.Random,
    hierarchy_c: float,
) -> Metrics:
    q = functional.q
    zero = [0.0] * dim
    values: List[float] = []
    zero_errors: List[float] = []
    even_errors: List[float] = []
    homogeneity_errors: List[float] = []
    residuals: List[float] = []
    signed_residuals: List[float] = []
    c_needed: List[float] = []
    nonnegativity_violations = 0
    monotonicity_violations = 0
    max_monotonicity_violation = 0.0
    hierarchy_violations = 0

    for _ in range(samples):
        x = random_vector(dim, rng)
        y = random_vector(dim, rng)

        qx = q(x)
        qy = q(y)
        qxy = q(add(x, y))
        values.extend([qx, qy, qxy])
        if qx < -1.0e-10 or qy < -1.0e-10 or qxy < -1.0e-10:
            nonnegativity_violations += 1

        residual = four_point_residual(q, x, y)
        residuals.append(abs(residual))
        signed_residuals.append(residual)

        zero_errors.append(abs(q(zero)))
        even_errors.append(abs(q(scale(-1.0, x)) - qx))
        homogeneity_errors.append(abs(q(scale(2.0, x)) - 4.0 * qx))

        kx = scale(0.5, x)
        monotone_gap = q(kx) - qx
        if monotone_gap > 1.0e-10:
            monotonicity_violations += 1
            max_monotonicity_violation = max(max_monotonicity_violation, monotone_gap)

        excess = qxy - qx - qy
        needed = safe_ratio(excess, math.sqrt(max(qx, 0.0) * max(qy, 0.0)))
        c_needed.append(max(0.0, needed))
        if excess > hierarchy_c * math.sqrt(max(qx, 0.0) * max(qy, 0.0)) + 1.0e-10:
            hierarchy_violations += 1

    failures: List[str] = []
    if nonnegativity_violations:
        failures.append("q_nonnegative")
    if max(zero_errors) > 1.0e-9:
        failures.append("q_zero")
    if max(even_errors) > 1.0e-8:
        failures.append("q_neg_symmetric")
    if max(homogeneity_errors) > 1.0e-7:
        failures.append("q_nat_square_homogeneous")
    if monotonicity_violations:
        failures.append("shrink_monotonicity")
    if hierarchy_violations:
        failures.append("hierarchy_consistency")
    if max(residuals) > 1.0e-7:
        failures.append("four_point_parallelogram")
    classification = classify(failures, max(residuals), max(homogeneity_errors), hierarchy_violations)
    coarse_gate_failures = tuple(f for f in failures if f != "four_point_parallelogram")
    passes_coarse_gate = len(coarse_gate_failures) == 0
    fails_only_four_point = passes_coarse_gate and "four_point_parallelogram" in failures

    return Metrics(
        name=functional.name,
        classification=classification,
        mean_abs_four_point=statistics.fmean(residuals),
        max_abs_four_point=max(residuals),
        p95_abs_four_point=percentile(residuals, 0.95),
        mean_signed_four_point=statistics.fmean(signed_residuals),
        min_value=min(values),
        max_zero_error=max(zero_errors),
        max_even_error=max(even_errors),
        max_two_homogeneity_error=max(homogeneity_errors),
        nonnegativity_violations=nonnegativity_violations,
        monotonicity_violations=monotonicity_violations,
        max_monotonicity_violation=max_monotonicity_violation,
        hierarchy_c_needed_max=percentile(c_needed, 0.999),
        hierarchy_violations=hierarchy_violations,
        coarse_gate_failures=coarse_gate_failures,
        passes_coarse_hierarchy_like_gate=passes_coarse_gate,
        fails_only_four_point_after_coarse_gate=fails_only_four_point,
        failed_hypotheses=tuple(failures),
        expected=functional.expected,
    )


def functionals(dim: int, rng: random.Random) -> List[Functional]:
    return [
        Functional(
        "quadratic_l2_squared",
        l2_sq,
        "passes: inner-product quadratic, residual should be roundoff",
        ),
        Functional(
        "coupled_quadratic_spd",
        coupled_quadratic_spd,
        "passes: coupling is allowed when still quadratic/SPD",
        ),
        Functional(
        "random_spd_quadratic",
        random_spd_quadratic(dim, rng),
        "passes: random seeded SPD quadratic remains parallelogram",
        ),
        Functional(
        "l1_like_squared",
        l1_squared,
        "fails four-point: norm-like hierarchy checks do not force Hilbert structure",
        ),
        Functional(
        "p_norm_squared_p3",
        p_norm_squared(3.0),
        "fails four-point: p != 2 is not parallelogram/Hilbert",
        ),
        Functional(
        "max_norm_squared",
        max_norm_squared,
        "fails four-point: max norm squared is two-homogeneous but non-Hilbert",
        ),
        Functional(
        "random_polyhedral_norm_squared",
        random_polyhedral_norm_squared(dim, rng, facets=max(4 * dim, 12)),
        "fails four-point: random polyhedral norm squared is a seeded near-miss",
        ),
        Functional(
        "coupled_l1_l2_two_homogeneous",
        coupled_l1_l2,
        "fails four-point despite two-homogeneity and nonnegative coupling",
        ),
        Functional(
        "mixed_two_homogeneous",
        mixed_two_homogeneous(dim, rng),
        "fails four-point: quadratic plus non-Hilbert two-homogeneous terms",
        ),
        Functional(
        "coupled_nonquadratic_quartic",
        coupled_nonquadratic,
        "fails scaling and four-point: coupling can break quadraticity directly",
        ),
    ]


def print_report(metrics: Sequence[Metrics], dim: int, samples: int, seed: int, hierarchy_c: float) -> None:
    print("defect four-point/parallelogram harness")
    print(f"dim={dim} samples={samples} seed={seed} hierarchy_c={hierarchy_c:g}")
    print("diagnostic only: no theorem/proof/Clay promotion claims")
    print()
    header = (
        "functional",
        "class",
        "mean|Δ4|",
        "max|Δ4|",
        "max hom2 err",
        "C_needed(.999)",
        "hier viol",
        "failed hypotheses",
    )
    print("{:<34} {:<40} {:>12} {:>12} {:>14} {:>14} {:>10}  {}".format(*header))
    for m in metrics:
        failed = ",".join(m.failed_hypotheses) if m.failed_hypotheses else "none"
        print(
            "{:<34} {:<40} {:>12.4g} {:>12.4g} {:>14.4g} "
            "{:>14.4g} {:>10}  {}".format(
                m.name,
                m.classification,
                m.mean_abs_four_point,
                m.max_abs_four_point,
                m.max_two_homogeneity_error,
                m.hierarchy_c_needed_max,
                m.hierarchy_violations,
                failed,
            )
        )

    print()
    coarse_witnesses = [m for m in metrics if m.fails_only_four_point_after_coarse_gate]
    if coarse_witnesses:
        print("coarse-check insufficiency witnesses")
        print(
            "These candidates pass nonnegativity/zero/evenness/two-homogeneity/"
            "shrink-monotonicity/hierarchy-C checks and fail only the four-point law."
        )
        for m in coarse_witnesses:
            print(f"- {m.name}: max|Δ4|={m.max_abs_four_point:.6g}, C_needed(.999)={m.hierarchy_c_needed_max:.6g}")
        print()
    print("interpretation")
    for m in metrics:
        print(f"- {m.name}: {m.expected}; observed failures: {', '.join(m.failed_hypotheses) or 'none'}")


def write_json(path: str, metrics: Sequence[Metrics], dim: int, samples: int, seed: int, hierarchy_c: float) -> None:
    payload = {
        "promotion": False,
        "diagnostic": "defect_four_point_counterexample_classifier",
        "dim": dim,
        "samples": samples,
        "seed": seed,
        "hierarchy_c": hierarchy_c,
        "metrics": [asdict(m) for m in metrics],
        "summary": {
            "passes": [m.name for m in metrics if not m.failed_hypotheses],
            "coarse_gate_definition": [
                "q_nonnegative",
                "q_zero",
                "q_neg_symmetric",
                "q_nat_square_homogeneous",
                "shrink_monotonicity",
                "hierarchy_consistency",
            ],
            "coarse_gate_false_accepts": [
                m.name for m in metrics if m.fails_only_four_point_after_coarse_gate
            ],
            "near_misses": [
                m.name for m in metrics if m.classification == "near_miss_two_homogeneous_counterexample"
            ],
            "counterexamples_to_coarse_checks": [
                m.name for m in metrics if m.classification == "counterexample_to_coarse_checks"
            ],
            "scaling_rejects": [
                m.name for m in metrics if m.classification == "reject_nonquadratic_scaling"
            ],
        },
    }
    with open(path, "w", encoding="utf-8") as handle:
        json.dump(payload, handle, indent=2, sort_keys=True)
        handle.write("\n")


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--dim", type=int, default=5)
    parser.add_argument("--samples", type=int, default=20_000)
    parser.add_argument("--seed", type=int, default=20260608)
    parser.add_argument(
        "--hierarchy-c",
        type=float,
        default=2.0,
        help="coarse hierarchy inequality constant C in Q(x+y)<=Q(x)+Q(y)+C sqrt(Qx Qy)",
    )
    parser.add_argument("--json", dest="json_path", help="write machine-readable metrics to this path")
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    if args.dim <= 0:
        raise SystemExit("--dim must be positive")
    if args.samples <= 0:
        raise SystemExit("--samples must be positive")
    if args.hierarchy_c < 0:
        raise SystemExit("--hierarchy-c must be nonnegative")

    candidate_rng = random.Random(args.seed)
    sample_rng = random.Random(args.seed + 1)
    metrics = [
        sample_metrics(fn, args.dim, args.samples, sample_rng, args.hierarchy_c)
        for fn in functionals(args.dim, candidate_rng)
    ]
    print_report(metrics, args.dim, args.samples, args.seed, args.hierarchy_c)
    if args.json_path:
        write_json(args.json_path, metrics, args.dim, args.samples, args.seed, args.hierarchy_c)
        print()
        print(f"wrote json: {args.json_path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
